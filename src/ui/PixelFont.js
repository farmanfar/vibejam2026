export const FONT_KEY = 'm5x7';

export class PixelFont {
  /**
   * Generate the m5x7 bitmap font atlas and register it with Phaser.
   * Call once in BootScene.create().
   */
  static init(scene) {
    const texW = COLS * CELL_W;
    const texH = ROWS * CELL_H;

    // Create canvas texture
    const canvas = document.createElement('canvas');
    canvas.width = texW;
    canvas.height = texH;
    const ctx = canvas.getContext('2d');

    // Render all glyphs as white pixels on transparent background
    const charList = this._buildCharList();
    charList.forEach((ch, index) => {
      const col = index % COLS;
      const row = Math.floor(index / COLS);
      const ox = col * CELL_W;
      const oy = row * CELL_H;
      const glyph = GLYPHS[ch];
      if (!glyph) return;

      for (let gy = 0; gy < GLYPH_H; gy++) {
        const bits = glyph[gy];
        for (let gx = 0; gx < GLYPH_W; gx++) {
          if (bits & (1 << (GLYPH_W - 1 - gx))) {
            ctx.fillStyle = '#ffffff';
            ctx.fillRect(ox + gx, oy + gy, 1, 1);
          }
        }
      }
    });

    // Add to Phaser texture manager
    scene.textures.addCanvas(FONT_KEY + '_atlas', canvas);

    // Build RetroFont config
    const config = {
      image: FONT_KEY + '_atlas',
      width: CELL_W,
      height: CELL_H,
      chars: charList.join(''),
      charsPerRow: COLS,
      spacing: { x: 0, y: 0 },
      offset: { x: 0, y: 0 },
    };

    // Register as bitmap font
    const fontData = GameObjects.RetroFont.Parse(scene, config);
    scene.cache.bitmapFont.add(FONT_KEY, fontData);
  }

  /** Ordered list of characters in the atlas, matching ASCII 32-126. */
  static _buildCharList() {
    const chars = [];
    for (let i = FIRST_CHAR; i <= LAST_CHAR; i++) {
      chars.push(String.fromCharCode(i));
    }
    return chars;
  }

  /**
   * Measure text dimensions at a given scale.
   * @param {string} text
   * @param {number} scale - integer multiplier (1=native 5x7, 2=10x14, etc.)
   * @returns {{ width: number, height: number }}
   */
  static measure(text, scale = 2) {
    const fontSize = GLYPH_H * scale;
    // BitmapText character width = CELL_W * (fontSize / GLYPH_H)
    const charW = CELL_W * scale;
    return {
      width: text.length * charW,
      height: fontSize,
    };
  }

  /** Native glyph dimensions */
  static get GLYPH_W() { return GLYPH_W; }
  static get GLYPH_H() { return GLYPH_H; }
  static get CELL_W() { return CELL_W; }
  static get CELL_H() { return CELL_H; }
}
